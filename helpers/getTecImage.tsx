export const getTecImage = (tec: string) =>{
    switch(tec){
        case 'ts':{
            return "tec/ts.png"
        }
        case 'react':{
            return "tec/react.png"
        }
        case 'azure':{
            return "tec/azure.png"
        }
        case 'csharp':{
            return "tec/csharp.png"
        }
        case 'flutter':{
            return "tec/flutter.png"
        }
        case 'python':{
            return "tec/python.png"
        }
        case 'java':{
            return "tec/java.png"
        }
        case 'c':{
            return "tec/c.png"
        }
        case 'node':{
            return "tec/node.png"
        }
    }
}